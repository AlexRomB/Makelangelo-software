package com.marginallyclever.convenience;

import java.awt.GridBagConstraints;

public class GridBagConstraintsValue extends GridBagConstraints {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -5147343984619074195L;

	public GridBagConstraintsValue() {
		ipadx=5;
	    ipady=0;
		anchor = GridBagConstraints.WEST;
		fill = GridBagConstraints.HORIZONTAL;
		weightx = 50;
		gridx = 1;
		gridy = 0;
	}
}
